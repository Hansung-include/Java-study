package assignment;
public class OperationEX2 {
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num);
        int val1 = num++;
        System.out.println(val1);
        System.out.println(num);
        int val2 = ++num;
        System.out.println(val2);
        System.out.println(num);
        int num2 = 3;
        System.out.println(num2);
        int val3 = num--;
        System.out.println(val3);
        System.out.println(num2);
        int val4 = --num;
        System.out.println(val4);
        System.out.println(num);

    }
}
